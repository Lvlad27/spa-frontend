class User {
    constructor(userName, password, name, surname, age, birthday, gender, hobbies) {
        this.userName = userName;
        this.password = password;
        this.name = name;
        this.surname = surname;
        this.age = age;
        this.birthday = birthday;
        this.gender = gender;
        this.hobbies = hobbies;
    }
}

export default User;
